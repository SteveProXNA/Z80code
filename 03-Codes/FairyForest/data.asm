; format:

; 1xxxxxxx
; ||||||||
; ||||````---- data (data set to the register)
; |||`-------- type (1 sets volume, 0 sets tone/noise)
; |``--------- channel (00 = ch 0, 01 = ch 1, 10 = ch 2 & 11 = ch 3)
; `----------- always 1

